package CSC1350_Fall24.Loops;

import java.util.Scanner;

public class ProblemSolving {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        //5 * 10
        int result = 0;
        do{
            System.out.println("what is 5 * 10?");
            result = in.nextInt();
        }while(result != 50);



    }
}
