package CSC1350_Fall24.Variables;

public class PrintVariables {
    public static void main(String[] args) {
        double doubleNumber = 3.5;
        int intNumber = 300;
        System.out.println(doubleNumber);
        System.out.println(intNumber);

        //reassign and print
        doubleNumber = 10;
        intNumber = 45;
        System.out.println(doubleNumber);
        System.out.println(intNumber);
    }
}
