package CSC1350_Fall24.Operators;

public class IntegerDivisionAndModulus {
    public static void main(String[] args) {

        int divideInts = 10 / 3;
        System.out.println(divideInts);

        int modulo = 10 % 3;
        System.out.println(modulo);


        double divideIntsToDouble = 10 / 3;
        System.out.println(divideIntsToDouble);

        double divideDoubles = 10.0 / 3.0;
        System.out.println(divideDoubles);
    }
}
